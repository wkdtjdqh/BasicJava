package e_oop;

public class Calculator {
	//5개 연산을 수행하는 5개의 메서드
	
	double add(double a, double b){
		return a+b;
	}
	
	double min(double a, double b){
		return a-b;
	}
	
	double div(double a, double b){
		return a/b;
	}
	
	double mul(double a, double b){
		return a*b;
	}
	
	double rem(double a, double b){
		return a%b;
	}
}
